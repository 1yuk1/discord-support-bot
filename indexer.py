def search_query(query_text, top_k=3):
    query_embedding = embedder.encode("query: " + query_text).tolist()
    results = collection.query(
        query_embeddings=[query_embedding],
        n_results=top_k,
        include=["documents", "metadatas"]
    )
    return results

def format_result(doc, meta):
    lines = []
    lines.append(f"\nКатегория: {meta.get('category', 'unknown')} | Приоритет: {meta.get('priority', 'medium')}")
    lines.append(f"Передать человеку: {'Да' if meta.get('transfer_to_human') else 'Нет'}")
    lines.append("-" * 40)
    lines.append(doc)
    return "\n".join(lines)


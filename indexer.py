    search_parts = [
        item['question'],
        ' '.join(item['synonyms']),
    ]
    
    # Добавляем ключевые слова из диагностики
    if for_llm.get("diagnostics"):
        search_parts.extend(for_llm["diagnostics"])
    
    # Добавляем ключевые слова из quick_answer
    if for_llm.get("quick_answer"):
        search_parts.append(for_llm["quick_answer"])
    
    # Добавляем problem описание
    if for_llm.get("problem"):
        search_parts.append(for_llm["problem"])
    
    search_text = ". ".join(search_parts)

    doc_lines = []
    doc_lines.append(f"## Проблема\n{for_llm.get('problem', item['question'])}")

    if for_llm.get("diagnostics"):
        doc_lines.append(f"\n## Диагностика\n" + "\n".join(f"- {q}" for q in for_llm["diagnostics"]))

    if for_llm.get("quick_answer"):
        doc_lines.append(f"\n## Быстрый ответ\n{for_llm['quick_answer']}")

    if for_llm.get("full_solution"):
        doc_lines.append(f"\n## Полное решение\n{for_llm['full_solution']}")

    if for_llm.get("steps"):
        doc_lines.append(f"\n## Инструкция по шагам\n" + "\n".join(for_llm["steps"]))

